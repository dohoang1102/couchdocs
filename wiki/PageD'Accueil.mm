Bienvenue sur le Wiki du [[http://incubator.apache.org/couchdb/|Projet Apache CouchDB]].

Apache CouchDB est une base de documents non structurés, distribuée, résistante aux pannes et accessible via une API REST HTTP/JSON. Parmi les autres fonctionnalités, elle fournit une réplication incrémentale robuste avec une détection et une résolution bi-directionnelle des conflits. Par ailleurs, elle peut être interrogée et indexée à travers un moteurs de vue qui utilise par défaut !Javascript comme langage de définition de vue.

Pour plus d'informations voir :

 * [[http://incubator.apache.org/couchdb/docs/intro.html|Introduction]]
 * [[http://incubator.apache.org/couchdb/docs/overview.html|Aperçu technique]]
 * [[http://svn.apache.org/viewvc/couchdb/trunk/README|LISEZMOI]]
 * [[http://svn.apache.org/viewvc/couchdb/trunk/NEWS|NOUVEAUX]]

