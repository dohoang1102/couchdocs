Apache [[http://couchdb.apache.org/|CouchDB]] は、スケーラブルでフォールトトレラントな、スキーマフリードキュメント指向データベースで, [[http://www.erlang.org/|Erlang]] で記述されています。[[http://couchdb.apache.org/|CouchDB]] は、大きなところから小さなところまで、従来のSQLデータベースが目の前の問題を解決するベストソリューションではない様々なアプリケーションに使われています。

 * 様々なプログラミングライブラリやツールからアクセスできる RESTful HTTP/JSON API
 * Futon: ブラウザベースのGUI
 * 堅牢で競合を検出/解決する双方向差分レプリケーション
 * !JavaScriptで記述できる差分Map/Reduce
 * MVCCを使ったすぐれたデータの完全性/信頼性
 * BLOB(Binary Large Objectts)をそのまま保存
 * 多くのプラットフォームに簡単にインストールできる。
 * 強くて活発なコミュニティ
 * 書籍、プレゼンテーション、ブログ、ウィキなどの様々なよいドキュメント

